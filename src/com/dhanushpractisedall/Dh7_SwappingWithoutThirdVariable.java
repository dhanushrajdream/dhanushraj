package com.dhanushpractisedall;

public class Dh7_SwappingWithoutThirdVariable {
	
	public static void main(String[] args) {
		
	    //swap without using third variable
		int a=10,b=20;
		
		a=a+b;    //a=10+20=30
		b=a-b;    //b=30-20=10
		a=a-b;    //a=30-10=20
		System.out.println(a);
		System.out.println(b);
		
		
		
		
		
	}

}
